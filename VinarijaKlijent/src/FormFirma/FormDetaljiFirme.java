    private void srediFormu() {
        lblID.setText("FirmaID: " + String.valueOf(f.getFirmaID()));
        txtPIB.setText(f.getPIB());
        txtNazivFirme.setText(f.getNazivFirme());
        txtAdresa.setText(f.getAdresa());
        txtBrojTelefona.setText(f.getBrojTelefona());
        txtEmail.setText(f.getEmail());
        txtPIB.setEnabled(false);
        txtNazivFirme.setEnabled(false);
        setTitle("Detalji firme");
        
        popuniFakture();
        
    }

    private void popuniFakture() {
        
        try {
            ArrayList<Faktura> fakture = ClientController.getInstance().getAllFaktura();

            ArrayList<Faktura> filtriraneFakture = new ArrayList<>();
            
            for(Faktura faktura : fakture){
                if(faktura.getFirma().getFirmaID().equals(f.getFirmaID())){
                    filtriraneFakture.add(faktura);
                }
            }
            
            cmbFakture.removeAllItems();

            for (Faktura faktura : filtriraneFakture) {
                cmbFakture.addItem(faktura);
            }
            
           // JOptionPane.showMessageDialog(rootPane, filtriraneFakture);
        }catch(Exception e){
            e.printStackTrace();
        }
    }

