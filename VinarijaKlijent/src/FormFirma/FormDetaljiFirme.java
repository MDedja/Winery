    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if (txtPIB.getText().isEmpty() || txtNazivFirme.getText().isEmpty()
                || txtAdresa.getText().isEmpty() || txtEmail.getText().isEmpty()
                || txtBrojTelefona.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
            return;
        }

        String adresa = txtAdresa.getText();
        String brojTelefona = txtBrojTelefona.getText();
        String email = txtEmail.getText();
        
        f.setAdresa(adresa);
        f.setBrojTelefona(brojTelefona);
        f.setEmail(email);

        try {
            ClientController.getInstance().updateFirma(f);
            FormPretragaFirme fp = (FormPretragaFirme) getParent();
            fp.refreshTable();
            fp.popuniFirme();
            JOptionPane.showMessageDialog(this, "Uspesno izmenjena firma.");
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(FormDetaljiFirme.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "obrisete ovu firmu?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().deleteFirma(f);
                FormPretragaFirme fp = (FormPretragaFirme) getParent();
                fp.refreshTable();
                fp.popuniFirme();
                JOptionPane.showMessageDialog(this, "Uspesno obrisana firma.");
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Ne mozete obrisati firmu koja ima fakturu!");
//                Logger.getLogger(FormDetaljiFirme.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btnObrisiActionPerformed


