    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (txtKolicina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti kolicinu!");
            return;
        }

        Vino v = (Vino) cmbVino.getSelectedItem();
        int kolicina = Integer.parseInt(txtKolicina.getText());
        double cenaStavke = Double.parseDouble(txtCenaStavke.getText());

        StavkaFakture sf = new StavkaFakture(null, -1, kolicina, cenaStavke, v);

        TableModelStavkeFakture tm = (TableModelStavkeFakture) tblStavkeFakture.getModel();
        tm.dodajStavku(sf);

        double cenaBezPDV = Double.parseDouble(txtCenaBezPDV.getText());

        txtCenaBezPDV.setText(String.valueOf(cenaBezPDV + sf.getCenaStavke()));

        cenaBezPDV = Double.parseDouble(txtCenaBezPDV.getText());

        txtCenaSaPDV.setText(String.valueOf(cenaBezPDV * (1.2)));

    }//GEN-LAST:event_btnDodajActionPerformed

    private void txtKolicinaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKolicinaKeyReleased

        if ((evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9')
                || evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {

            if (txtKolicina.getText().isEmpty()) {
                txtCenaStavke.setText("0.00");
                return;
            }

            int kolicina = Integer.parseInt(txtKolicina.getText());

            if (kolicina <= 0) {
                txtKolicina.setText("");
                return;
            }

            Vino v = (Vino) cmbVino.getSelectedItem();

            txtCenaStavke.setText(String.valueOf(v.getCena() * kolicina));

        } else {
            txtKolicina.setText("");
            txtCenaStavke.setText("0.00");
        }

    }//GEN-LAST:event_txtKolicinaKeyReleased

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int row = tblStavkeFakture.getSelectedRow();

        if (row >= 0) {
            TableModelStavkeFakture tm = (TableModelStavkeFakture) tblStavkeFakture.getModel();

            StavkaFakture sf = tm.getStavka(row);

            double cenaBezPDV = Double.parseDouble(txtCenaBezPDV.getText());

            txtCenaBezPDV.setText(String.valueOf(cenaBezPDV - sf.getCenaStavke()));

            cenaBezPDV = Double.parseDouble(txtCenaBezPDV.getText());

            txtCenaSaPDV.setText(String.valueOf(cenaBezPDV * (1.2)));

            tm.obrisiStavku(row);
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void cmbVinoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbVinoItemStateChanged

        if (cmbVino.getSelectedItem() != null) {
            if (txtKolicina.getText().isEmpty()) {
                txtCenaStavke.setText("0.00");
                return;
            }

            int kolicina = Integer.parseInt(txtKolicina.getText());

            if (kolicina <= 0) {
                txtKolicina.setText("");
                return;
            }

            Vino v = (Vino) cmbVino.getSelectedItem();

            txtCenaStavke.setText(String.valueOf(v.getCena() * kolicina));
        }

    }//GEN-LAST:event_cmbVinoItemStateChanged

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        try {
            Firma firma = (Firma) cmbFirma.getSelectedItem();
            double cenaBezPDV = Double.parseDouble(txtCenaBezPDV.getText());
            double cenaSaPDV = Double.parseDouble(txtCenaSaPDV.getText());

            TableModelStavkeFakture tm = (TableModelStavkeFakture) tblStavkeFakture.getModel();

            Faktura f = new Faktura(null, new Date(), cenaBezPDV, 20,
                    cenaSaPDV, firma, a, tm.getLista());

            ClientController.getInstance().addFaktura(f);
            resetujFormu();
            JOptionPane.showMessageDialog(this, "Uspesno sacuvana faktura!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

