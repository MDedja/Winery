    private void srediFormu() {
        popuniFirme();
        popuniVinarije();
        txtCenaStavke.setEnabled(false);
        txtCenaSaPDV.setEnabled(false);
        txtCenaBezPDV.setEnabled(false);
        txtCenaStavke.setText("0.00");
        txtCenaSaPDV.setText("0.00");
        txtCenaBezPDV.setText("0.00");
        lblPDV.setText("20%");
        tblStavkeFakture.setModel(new TableModelStavkeFakture());
        Thread th = new Thread(this);
        th.start();
    }

    public void popuniFirme() {
        try {
            ArrayList<Firma> firme = ClientController.getInstance().getAllFirma();

            cmbFirma.removeAllItems();

            for (Firma firma : firme) {
                cmbFirma.addItem(firma);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void popuniVinarije() {
        try {
            ArrayList<Vinarija> vinarije = ClientController.getInstance().getAllVinarija();

            cmbVinarija.removeAllItems();

            for (Vinarija vinarija : vinarije) {
                cmbVinarija.addItem(vinarija);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniVina(Vinarija v) {
        try {
            ArrayList<Vino> listaVina = ClientController.getInstance().getAllVino(v);

            cmbVino.removeAllItems();

            for (Vino vino : listaVina) {
                cmbVino.addItem(vino);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void resetujFormu() {
        TableModelStavkeFakture tm = (TableModelStavkeFakture) tblStavkeFakture.getModel();
        tm.isprazniTabelu();
        txtCenaStavke.setText("0.00");
        txtCenaSaPDV.setText("0.00");
        txtCenaBezPDV.setText("0.00");
        txtKolicina.setText("");
    }

    @Override
    public void run() {
        while(true){
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");
            lblSat.setText("Datum i vreme: " + sdf.format(new Date()));
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

